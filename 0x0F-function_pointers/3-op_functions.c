#include "3-calc.h"

/**
 * op_add - returns addition of two numbers
 * @a: number to be added
 * @b: number to be added
 * Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: integer
 * @b: integer
 * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: integer
 * @b: integer
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: integer
 * @b: integer
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two numbers
 * @a: integer
 * @b: integer
 * Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}

